#Modulo: Operaciones_Avanzadas.py
import math

def Metodo_Seno(a):
    return math.sin(a)
def Metodo_Coseno(a):
    return math.cos(a)
def Metodo_Raiz(a):
    return math.sqrt(a)
def Metodo_Factorial(a):
    return math.factorial(a)
def Metodo_Imprimir_Avanzados(p):
    print("Seno         : ",Metodo_Seno(p))
    print("Coseno       : ",Metodo_Coseno(p))
    print("Raiz Cuadrada: ",Metodo_Raiz(p))
    print("Factorial    : ",Metodo_Factorial(p))