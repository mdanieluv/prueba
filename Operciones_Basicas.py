#Modulo: Operaciones_Basicas.py
def Metodo_Suma(a,b):
    return a+b
def Metodo_Resta(a,b):
    return a-b
def Metodo_Multiplica(a,b):
    return a*b
def Metodo_Divide(a,b):
    return a/b
def Metodo_Imprimir_Basicas(p,q):
    print("Suma          : ",Metodo_Suma(p,q))
    print("Resta         : ",Metodo_Resta(p,q))
    print("Multiplicación: ",Metodo_Multiplica(p,q))
    print("División      : ",Metodo_Divide(p,q))